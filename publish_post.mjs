這些理論離我們並不遙遠。

根據研究，臺灣的**代間流動性**（父母社會地位對子女的影響程度）在過去數十年間呈現停滯甚至下降的趨勢。幾個值得關注的現象：

**教育的篩選功能**：頂尖大學的學生，來自都會區與高收入家庭的比例遠高於其人口比例。學測考的不只是努力，更考的是家庭能否提供補習、試題練習、文化刺激。

**居住地的決定性影響**：你住在哪個行政區，往往決定了你就讀哪所學校、認識哪些人、接觸什麼樣的資源。城鄉差距不只是地理問題，而是機會結構的差距。

**工作的極化**：技術進步帶來高薪的知識工作，同時創造大量低薪的服務業職位，中間層逐漸萎縮。階層間的距離，正在拉大。

